export function resolveLanguage(value?: string | null): AppLanguage {
  if (!value) {
    return "en";
  }

  const normalized = value.toLowerCase();
  return supportedLanguages.some((language) => language.value === normalized) ? (normalized as AppLanguage) : "en";
}

function getNestedValue(source: TranslationTree, key: string): string | TranslationTree | undefined {
  return key.split(".").reduce<string | TranslationTree | undefined>((current, part) => {
    if (!current || typeof current === "string" || !(part in current)) {
      return undefined;
    }

    return current[part];
  }, source);
}

function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template;
  }

  return Object.entries(values).reduce(
    (current, [key, value]) => current.replaceAll(`{${key}}`, String(value)),
    template
  );
}

export function translate(language: AppLanguage, key: string, values?: Record<string, string | number>) {
  const dictionary = dictionaries[language] ?? dictionaries.en;
  const fallback = dictionaries.en;
  const directValue = getNestedValue(dictionary, key);
  const fallbackValue = getNestedValue(fallback, key);
  const template = typeof directValue === "string" ? directValue : typeof fallbackValue === "string" ? fallbackValue : key;

  return interpolate(template, values);
}

export function createTranslator(language: AppLanguage) {
  return (key: string, values?: Record<string, string | number>) => translate(language, key, values);
}

export function getLanguageOption(language: AppLanguage) {
  return supportedLanguages.find((option) => option.value === language) ?? supportedLanguages[0];
}
